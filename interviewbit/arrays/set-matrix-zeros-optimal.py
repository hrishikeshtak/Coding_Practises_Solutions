"""
Given an m x n matrix of 0s and 1s, if an element is 0,
set its entire row and column to 0.
"""
# Complexity : O(N * M), O(1)


class Solution:
    # @param A : list of list of integers
    # @return the same list modified
    def setZeroes(self, A):
        N = len(A)
        M = len(A[0])
        # print(N, M)
        rows = False
        cols = False
        # update first row and first col if 0 discovered
        for i in range(0, N):
            for j in range(0, M):
                if i == 0 and A[i][j] == 0:
                    rows = True
                if j == 0 and A[i][j] == 0:
                    cols = True
                if A[i][j] == 0:
                    A[i][0] = 0
                    A[0][j] = 0

        for i in range(1, N):
            for j in range(1, M):
                if A[0][j] == 0 or A[i][0] == 0:
                    A[i][j] = 0

        if rows:
            for i in range(0, M):
                A[0][i] = 0
        if cols:
            for i in range(0, N):
                A[i][0] = 0
        # print(rows)
        # print(cols)
        return A


